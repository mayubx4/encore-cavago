function LocationBottomSheet(bottomSheet: BottomSheetStateProps) {
  const { hostId } = useParams();
  const getHostProfileQuery = hostApi.getHostProfile.useQuery(hostId);
  const {
    data: hostProfile,
  } = getHostProfileQuery;

  return (
    <BottomSheet fullscreen title={null} {...bottomSheet}>
      <div className="location-content">
        <h2>Location</h2>
        <div className="map">
          <LocationMobile
            pos={{
              lat: Number(hostProfile?.overview_quick_view?.latitude),
              lng: Number(hostProfile?.overview_quick_view?.longitude),
            }}
            header={false}
          />
        </div>
        {(hostProfile?.facility_transports || []).map((ft) => (
          <>
            <h3>{ft.transport_mode}</h3>
            <p>
              {ft.description}
            </p>
          </>
        ))}
      </div>
    </BottomSheet>
  );
}
