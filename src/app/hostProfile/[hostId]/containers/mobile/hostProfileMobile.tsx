function AllActivitiesBottomSheet({ facilityId, ...bottomSheet }:
  BottomSheetStateProps & { facilityId: number }) {
  const activitiesQuery = homeApi.getActivities.useInfiniteQuery(
    {
      facility_id: facilityId,
      limit: 10,
    },
    { enabled: !!facilityId },
  );

  return (
    <BottomSheet fullscreen title={null} {...bottomSheet}>
      <QueryHandler queries={[activitiesQuery]}>
        <div className="activities-content">
          <h2>
            Activities with this host
          </h2>
          <InfiniteScroll
            next={activitiesQuery.fetchNextPage}
            hasMore={!!activitiesQuery.hasNextPage}
            loader={<Animations name="cavagoLoader" style={{ width: 100, height: 100 }} />}
            scrollThreshold={1}
            dataLength={activitiesQuery.data?.length || 0}
          >
            <div className="activities-list">
              {activitiesQuery.data?.map((a, i) => (
                <ActivityCard key={i} {...parseActivityPropsFromSchema(a)} />
              ))}
            </div>
          </InfiniteScroll>
        </div>
      </QueryHandler>
    </BottomSheet>
  );
}
